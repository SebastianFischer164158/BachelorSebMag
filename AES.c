void getRoundKey(int* src, int* dst, int round){
  //getRoundKey() is used to retrieve the current round key corresponding to a round, in AES
  //argument1: int array
  //argument2: int array
  //argument3: integer
    int running = round;
    if (round != 0){

        int rconArray[10] = {0x01, 0x02, 0x04, 0x08, 0x10, 0x20, 0x40, 0x80, 0x1B, 0x36};

        int c0[4] = {0};
        int c1[4] = {0};
        int c2[4] = {0};
        int c3[4] = {0};

        for (int i = 0; i < 4; i++){
            c0[i] = src[i];
            c1[i] = src[i+4];
            c2[i] = src[i+8];
            c3[i] = src[i+12];
        }

        int temp[4] = {0};
        int roundKey[16] = {0};

        for (int j = 0; j < running; j++){

        int addRound[4] = {rconArray[j], 0x00, 0x00, 0x00};

            for (int k = 0; k < 4; k++){
                if (k == 0){
                    copyArray(c3, temp,4);
                    rotWord(temp,4);
                    subBytes(temp,4);
                    xorArrays(temp,addRound);
                    xorArrays(temp,c0);
                    copyArray(temp,c0,4);
                } else if (k == 1){
                    xorArrays(temp,c1);
                    copyArray(temp,c1,4);
                } else if (k == 2){
                    xorArrays(temp,c2);
                    copyArray(temp,c2,4);
                } else if (k == 3){
                    xorArrays(temp,c3);
                    copyArray(temp,c3,4);
                }
            }
            memcpy(roundKey, c0, 4 * sizeof(int));
            memcpy(&roundKey[4], c1, 4 * sizeof(int));
            memcpy(&roundKey[8], c2, 4 * sizeof(int));
            memcpy(&roundKey[12], c3, 4 * sizeof(int));
        }
        copyArray(roundKey,dst,16);
    } else {
        copyArray(src,dst,16);
    }
}

